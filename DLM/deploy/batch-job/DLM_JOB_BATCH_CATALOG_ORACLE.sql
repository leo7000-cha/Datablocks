           , A.TABLE_NAME
           , A.CONSTRAINT_TYPE
           , COLUMN_NAME
           , POSITION
           , CASE WHEN A.CONSTRAINT_TYPE = ''P'' THEN ''Y'' END AS PK
      FROM ALL_CONSTRAINTS A
         , ALL_CONS_COLUMNS B
      WHERE UPPER(A.OWNER) IN (#{TARGET_SCHEMA_LIST})
