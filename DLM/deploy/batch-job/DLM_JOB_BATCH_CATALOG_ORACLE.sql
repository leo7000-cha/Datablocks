    , AA.OWNER
    , AA.TABLE_NAME
    , AA.COLUMN_NAME
    , AA.COLUMN_ID
    , CC.PK
    , CC.POSITION AS PK_POSITION
    , AA.DATA_TYPE AS FULL_DATA_TYPE
