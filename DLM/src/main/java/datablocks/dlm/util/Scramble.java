    public static String getScrResult(String str, String scrtype) {//System.out.println("getScrResult   str:"+str+"  scrtype:"+scrtype);
        if (str == null || str.trim().isEmpty()) return str;
        String rst = str;
        if (scrtype.startsWith("FIXED")) {
            rst = scrtype.substring(6);
        } else if (scrtype.startsWith("SCRAMBLE")) {
            String[] parts = scrtype.split("_");
            String detail = parts[1];
            String fromTo = parts[2];
            if (detail.equals("NORMAL")) {
                if (fromTo.equals("ALL")) {
                    rst = INSTANCE.scrambleString(str);
                } else if (fromTo.startsWith("LAST")) {
                    rst = INSTANCE.replaceLastNChars(str, Integer.parseInt(fromTo.substring(4)));
                } else if (fromTo.startsWith("FIRST")) {
                    rst = INSTANCE.replaceFirstNChars(str, Integer.parseInt(fromTo.substring(5)));
                } else if (fromTo.startsWith("AFTER")) {
                    rst = INSTANCE.replaceSubstring(str, Integer.parseInt(fromTo.substring(5)));
                }
            } else if (detail.equals("RRN")) {
                if (str.length() >= 13) {
                    if (fromTo.equals("ALL")) {
                        rst = INSTANCE.scrambleRrn(str);
                    } else {
                        rst = INSTANCE.scrambleRrn7(str);
                    }
                }
                else
                    rst = INSTANCE.scrambleBizNo(str);
            } else if (detail.equals("CORPNO")) {
                rst = INSTANCE.scrambleCorpNo(str);
            } else if (detail.equals("BIZNO")) {
                rst = INSTANCE.scrambleBizNo(str);
            } else if (detail.equals("YYMMDD")) {
                if (str.length() == 8)
                    rst = INSTANCE.scrambleYymmdd(str);
                else
                    rst = INSTANCE.scrambleYmd(str);
            } else if (detail.equals("EMAIL")) {
                rst = INSTANCE.scrambleEmail(str);
            } else {
                rst = INSTANCE.scrambleString(str);
            }
        }
        if(StrUtil.checkString(rst)){
            return str;
        }else {
            return rst;
        }
    }
