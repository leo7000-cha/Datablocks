COMMENT ON TABLE  TBL_ACCESS_LOG                    IS 'X-Audit 접속기록 Master (월 RANGE 파티션)';
COMMENT ON COLUMN TBL_ACCESS_LOG.hash_value         IS 'SHA-256 해시 (위변조 방지, 안전성확보조치 8조 3항)';
