-- 1) Master — TBL_ACCESS_LOG (월 RANGE 파티션)
DROP TABLE IF EXISTS TBL_ACCESS_LOG CASCADE;
CREATE TABLE TBL_ACCESS_LOG (
    log_id             BIGINT        GENERATED BY DEFAULT AS IDENTITY,
    -- WHO
    source_system_id   VARCHAR(36),
    user_account       VARCHAR(100),
    user_name          VARCHAR(100),
    department         VARCHAR(100),
    -- WHEN
    access_time        TIMESTAMP(3)  NOT NULL,
    -- WHERE
    client_ip          VARCHAR(45),
    session_id         VARCHAR(100),
    -- WHAT
    action_type        VARCHAR(20)   NOT NULL,
    target_db          VARCHAR(100),
    target_schema      VARCHAR(100),
    target_table       VARCHAR(200),
    affected_rows      INTEGER       DEFAULT 0,
    result_code        VARCHAR(10)   DEFAULT 'SUCCESS',
    -- PII
    pii_type_codes     VARCHAR(200),
    pii_grade          CHAR(1),
    pii_detected_flag  CHAR(1)       DEFAULT 'N',
    -- 수집 메타
    collect_type       VARCHAR(20),
    access_channel     VARCHAR(20)   DEFAULT 'WAS',
    -- 무결성
    hash_value         VARCHAR(64),
    prev_hash          VARCHAR(64),
    collected_at       TIMESTAMP(3)  DEFAULT CURRENT_TIMESTAMP NOT NULL,
    partition_key      VARCHAR(8),
    -- WAS HTTP 컨텍스트
    req_id             VARCHAR(36),
    service_name       VARCHAR(50),
    menu_id            VARCHAR(100),
    uri                VARCHAR(500),
    http_method        VARCHAR(10),
    http_status        INTEGER,
    duration_ms        BIGINT,
    CONSTRAINT pk_access_log PRIMARY KEY (log_id, access_time)
)
PARTITION BY RANGE (access_time);

