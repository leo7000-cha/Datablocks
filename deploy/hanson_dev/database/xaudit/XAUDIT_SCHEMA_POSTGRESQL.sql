-- 2) Sidecar — TBL_ACCESS_LOG_DETAIL (월 RANGE 파티션)
DROP TABLE IF EXISTS TBL_ACCESS_LOG_DETAIL CASCADE;
CREATE TABLE TBL_ACCESS_LOG_DETAIL (
    log_id           BIGINT        NOT NULL,
    access_time      TIMESTAMP(3)  NOT NULL,
    req_id           VARCHAR(36),
    sql_id           VARCHAR(255),
    sql_text         TEXT,
    bind_params      TEXT,
    search_condition VARCHAR(4000),
    target_columns   VARCHAR(4000),
    full_uri         VARCHAR(2000),
    user_agent       VARCHAR(500),
    error_message    VARCHAR(500),
    collected_at     TIMESTAMP(3)  DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT pk_access_log_detail PRIMARY KEY (log_id, access_time)
)
PARTITION BY RANGE (access_time);

CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202601 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-01-01') TO ('2026-02-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202602 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-02-01') TO ('2026-03-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202603 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-03-01') TO ('2026-04-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202604 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-04-01') TO ('2026-05-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202605 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-05-01') TO ('2026-06-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202606 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-06-01') TO ('2026-07-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202607 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-07-01') TO ('2026-08-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202608 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-08-01') TO ('2026-09-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_p202609 PARTITION OF TBL_ACCESS_LOG_DETAIL FOR VALUES FROM ('2026-09-01') TO ('2026-10-01');
CREATE TABLE TBL_ACCESS_LOG_DETAIL_pdefault PARTITION OF TBL_ACCESS_LOG_DETAIL DEFAULT;

COMMENT ON TABLE  TBL_ACCESS_LOG_DETAIL          IS 'X-Audit 접속기록 상세 sidecar (월 RANGE 파티션)';
COMMENT ON COLUMN TBL_ACCESS_LOG_DETAIL.sql_text IS 'SQL 풀 원문 (전자금융 13조9항 대응)';

CREATE INDEX idx_ald_req  ON TBL_ACCESS_LOG_DETAIL (req_id);
CREATE INDEX idx_ald_time ON TBL_ACCESS_LOG_DETAIL (access_time);

-- 3) 통합 조회 뷰
DROP VIEW IF EXISTS V_ACCESS_LOG_UNIFIED;
CREATE VIEW V_ACCESS_LOG_UNIFIED AS
SELECT
    a.log_id, a.source_system_id, a.user_account, a.user_name, a.department,
    a.access_time, a.client_ip, a.session_id,
    a.action_type, a.target_db, a.target_schema, a.target_table,
    a.affected_rows, a.result_code,
    a.pii_type_codes, a.pii_grade, a.pii_detected_flag,
    a.collect_type, a.access_channel,
    a.hash_value, a.prev_hash, a.collected_at, a.partition_key,
    a.req_id, a.service_name, a.menu_id, a.uri, a.http_method,
    a.http_status, a.duration_ms,
    d.sql_id, d.sql_text, d.bind_params, d.search_condition,
    d.target_columns, d.full_uri, d.user_agent, d.error_message
FROM TBL_ACCESS_LOG a
LEFT JOIN TBL_ACCESS_LOG_DETAIL d
       ON a.log_id = d.log_id AND a.access_time = d.access_time;

